-- =============================================================================
-- V1 — Core schema for the E-Commerce Order Management System.
--
-- This single migration owns the full schema and grows one vertical slice at a
-- time as the system is built (auth → catalog/inventory → cart/pricing →
-- order/payment → fulfillment → returns). Seed data lives in V2__seed.sql.
--
-- DDL is written to run unchanged on both PostgreSQL 16 (app) and H2 in
-- PostgreSQL-compatibility mode (tests): identity columns, NUMERIC(19,2) money,
-- TIMESTAMP, and TEXT only — no engine-specific types.
-- =============================================================================

-- -----------------------------------------------------------------------------
-- Users & authentication
-- -----------------------------------------------------------------------------
CREATE TABLE users (
    id            BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version       BIGINT       NOT NULL DEFAULT 0,
    created_at    TIMESTAMP    NOT NULL,
    updated_at    TIMESTAMP    NOT NULL,
    email         VARCHAR(255) NOT NULL,
    password_hash VARCHAR(255) NOT NULL,
    display_name  VARCHAR(120) NOT NULL,
    role          VARCHAR(32)  NOT NULL,
    CONSTRAINT uq_users_email UNIQUE (email),
    CONSTRAINT ck_users_role CHECK (role IN ('ADMIN', 'CUSTOMER', 'WAREHOUSE_STAFF'))
);

-- -----------------------------------------------------------------------------
-- Catalog: categories (self-referencing hierarchy) and products
-- -----------------------------------------------------------------------------
CREATE TABLE categories (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version    BIGINT       NOT NULL DEFAULT 0,
    created_at TIMESTAMP    NOT NULL,
    updated_at TIMESTAMP    NOT NULL,
    name       VARCHAR(120) NOT NULL,
    parent_id  BIGINT,
    CONSTRAINT fk_categories_parent FOREIGN KEY (parent_id) REFERENCES categories (id)
);

CREATE TABLE products (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version      BIGINT        NOT NULL DEFAULT 0,
    created_at   TIMESTAMP     NOT NULL,
    updated_at   TIMESTAMP     NOT NULL,
    sku          VARCHAR(64)   NOT NULL,
    name         VARCHAR(200)  NOT NULL,
    description  TEXT,
    base_price   NUMERIC(19,2) NOT NULL,
    tax_category VARCHAR(32)   NOT NULL DEFAULT 'STANDARD',
    active       BOOLEAN       NOT NULL DEFAULT TRUE,
    category_id  BIGINT,
    CONSTRAINT uq_products_sku UNIQUE (sku),
    CONSTRAINT fk_products_category FOREIGN KEY (category_id) REFERENCES categories (id),
    CONSTRAINT ck_products_base_price CHECK (base_price >= 0)
);

CREATE INDEX idx_products_category ON products (category_id);

-- -----------------------------------------------------------------------------
-- Inventory: warehouses and per-product stock levels
-- -----------------------------------------------------------------------------
CREATE TABLE warehouses (
    id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version    BIGINT       NOT NULL DEFAULT 0,
    created_at TIMESTAMP    NOT NULL,
    updated_at TIMESTAMP    NOT NULL,
    code       VARCHAR(32)  NOT NULL,
    name       VARCHAR(120) NOT NULL,
    region     VARCHAR(64),
    priority   INTEGER      NOT NULL DEFAULT 100,
    CONSTRAINT uq_warehouses_code UNIQUE (code)
);

CREATE TABLE stock_levels (
    id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version           BIGINT  NOT NULL DEFAULT 0,
    created_at        TIMESTAMP NOT NULL,
    updated_at        TIMESTAMP NOT NULL,
    product_id        BIGINT  NOT NULL,
    warehouse_id      BIGINT  NOT NULL,
    quantity_on_hand  INTEGER NOT NULL DEFAULT 0,
    quantity_reserved INTEGER NOT NULL DEFAULT 0,
    CONSTRAINT fk_stock_product   FOREIGN KEY (product_id)   REFERENCES products (id),
    CONSTRAINT fk_stock_warehouse FOREIGN KEY (warehouse_id) REFERENCES warehouses (id),
    CONSTRAINT uq_stock_product_warehouse UNIQUE (product_id, warehouse_id),
    CONSTRAINT ck_stock_on_hand  CHECK (quantity_on_hand >= 0),
    CONSTRAINT ck_stock_reserved CHECK (quantity_reserved >= 0 AND quantity_reserved <= quantity_on_hand)
);

-- -----------------------------------------------------------------------------
-- Pricing: tax rates and coupons
-- -----------------------------------------------------------------------------
CREATE TABLE tax_rates (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version      BIGINT       NOT NULL DEFAULT 0,
    created_at   TIMESTAMP    NOT NULL,
    updated_at   TIMESTAMP    NOT NULL,
    tax_category VARCHAR(32)  NOT NULL,
    region       VARCHAR(64)  NOT NULL,
    rate_percent NUMERIC(6,3) NOT NULL,
    CONSTRAINT uq_tax_category_region UNIQUE (tax_category, region),
    CONSTRAINT ck_tax_rate_percent CHECK (rate_percent >= 0)
);

CREATE TABLE coupons (
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version          BIGINT        NOT NULL DEFAULT 0,
    created_at       TIMESTAMP     NOT NULL,
    updated_at       TIMESTAMP     NOT NULL,
    code             VARCHAR(64)   NOT NULL,
    type             VARCHAR(16)   NOT NULL,
    discount_value   NUMERIC(19,2) NOT NULL,
    min_order_amount NUMERIC(19,2) NOT NULL DEFAULT 0,
    valid_from       TIMESTAMP,
    valid_to         TIMESTAMP,
    max_redemptions  INTEGER,
    times_redeemed   INTEGER       NOT NULL DEFAULT 0,
    active           BOOLEAN       NOT NULL DEFAULT TRUE,
    CONSTRAINT uq_coupons_code UNIQUE (code),
    CONSTRAINT ck_coupons_type CHECK (type IN ('PERCENT', 'FIXED')),
    CONSTRAINT ck_coupons_value CHECK (discount_value >= 0),
    CONSTRAINT ck_coupons_redeemed CHECK (times_redeemed >= 0)
);

-- -----------------------------------------------------------------------------
-- Cart: one active cart per customer (enforced in the service layer)
-- -----------------------------------------------------------------------------
CREATE TABLE carts (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version     BIGINT      NOT NULL DEFAULT 0,
    created_at  TIMESTAMP   NOT NULL,
    updated_at  TIMESTAMP   NOT NULL,
    customer_id BIGINT      NOT NULL,
    status      VARCHAR(20) NOT NULL,
    coupon_code VARCHAR(64),
    CONSTRAINT fk_carts_customer FOREIGN KEY (customer_id) REFERENCES users (id),
    CONSTRAINT ck_carts_status CHECK (status IN ('ACTIVE', 'CHECKED_OUT', 'ABANDONED'))
);

CREATE INDEX idx_carts_customer_status ON carts (customer_id, status);

CREATE TABLE cart_items (
    id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version             BIGINT        NOT NULL DEFAULT 0,
    created_at          TIMESTAMP     NOT NULL,
    updated_at          TIMESTAMP     NOT NULL,
    cart_id             BIGINT        NOT NULL,
    product_id          BIGINT        NOT NULL,
    quantity            INTEGER       NOT NULL,
    unit_price_snapshot NUMERIC(19,2) NOT NULL,
    CONSTRAINT fk_cart_items_cart    FOREIGN KEY (cart_id)    REFERENCES carts (id),
    CONSTRAINT fk_cart_items_product FOREIGN KEY (product_id) REFERENCES products (id),
    CONSTRAINT uq_cart_items_cart_product UNIQUE (cart_id, product_id),
    CONSTRAINT ck_cart_items_quantity CHECK (quantity > 0)
);

-- -----------------------------------------------------------------------------
-- Orders, order lines, payments, and inventory reservations
-- -----------------------------------------------------------------------------
CREATE TABLE orders (
    id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version        BIGINT        NOT NULL DEFAULT 0,
    created_at     TIMESTAMP     NOT NULL,
    updated_at     TIMESTAMP     NOT NULL,
    customer_id    BIGINT        NOT NULL,
    status         VARCHAR(20)   NOT NULL,
    subtotal       NUMERIC(19,2) NOT NULL,
    discount_total NUMERIC(19,2) NOT NULL,
    tax_total      NUMERIC(19,2) NOT NULL,
    shipping_total NUMERIC(19,2) NOT NULL,
    grand_total    NUMERIC(19,2) NOT NULL,
    coupon_code    VARCHAR(64),
    placed_at      TIMESTAMP     NOT NULL,
    CONSTRAINT fk_orders_customer FOREIGN KEY (customer_id) REFERENCES users (id),
    CONSTRAINT ck_orders_status CHECK (status IN
        ('PLACED', 'CONFIRMED', 'PACKED', 'SHIPPED', 'DELIVERED', 'RETURNED', 'CANCELLED'))
);

CREATE INDEX idx_orders_customer ON orders (customer_id);

CREATE TABLE order_items (
    id                     BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version                BIGINT        NOT NULL DEFAULT 0,
    created_at             TIMESTAMP     NOT NULL,
    updated_at             TIMESTAMP     NOT NULL,
    order_id               BIGINT        NOT NULL,
    product_id             BIGINT        NOT NULL,
    product_sku            VARCHAR(64)   NOT NULL,
    product_name           VARCHAR(200)  NOT NULL,
    quantity               INTEGER       NOT NULL,
    unit_price             NUMERIC(19,2) NOT NULL,
    line_discount          NUMERIC(19,2) NOT NULL,
    line_tax               NUMERIC(19,2) NOT NULL,
    line_total             NUMERIC(19,2) NOT NULL,
    allocated_warehouse_id BIGINT,
    CONSTRAINT fk_order_items_order     FOREIGN KEY (order_id)   REFERENCES orders (id),
    CONSTRAINT fk_order_items_product   FOREIGN KEY (product_id) REFERENCES products (id),
    CONSTRAINT fk_order_items_warehouse FOREIGN KEY (allocated_warehouse_id) REFERENCES warehouses (id),
    CONSTRAINT ck_order_items_quantity CHECK (quantity > 0)
);

CREATE INDEX idx_order_items_order ON order_items (order_id);

CREATE TABLE payments (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version         BIGINT        NOT NULL DEFAULT 0,
    created_at      TIMESTAMP     NOT NULL,
    updated_at      TIMESTAMP     NOT NULL,
    order_id        BIGINT        NOT NULL,
    amount          NUMERIC(19,2) NOT NULL,
    status          VARCHAR(20)   NOT NULL,
    method          VARCHAR(40),
    gateway_ref     VARCHAR(80),
    idempotency_key VARCHAR(100)  NOT NULL,
    CONSTRAINT fk_payments_order FOREIGN KEY (order_id) REFERENCES orders (id),
    CONSTRAINT uq_payments_idempotency UNIQUE (idempotency_key),
    CONSTRAINT ck_payments_status CHECK (status IN ('APPROVED', 'DECLINED'))
);

CREATE TABLE inventory_reservations (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version      BIGINT      NOT NULL DEFAULT 0,
    created_at   TIMESTAMP   NOT NULL,
    updated_at   TIMESTAMP   NOT NULL,
    order_id     BIGINT,
    product_id   BIGINT      NOT NULL,
    warehouse_id BIGINT      NOT NULL,
    quantity     INTEGER     NOT NULL,
    status       VARCHAR(20) NOT NULL,
    expires_at   TIMESTAMP,
    CONSTRAINT fk_reservations_order     FOREIGN KEY (order_id)     REFERENCES orders (id),
    CONSTRAINT fk_reservations_product   FOREIGN KEY (product_id)   REFERENCES products (id),
    CONSTRAINT fk_reservations_warehouse FOREIGN KEY (warehouse_id) REFERENCES warehouses (id),
    CONSTRAINT ck_reservations_status CHECK (status IN ('ACTIVE', 'CONFIRMED', 'RELEASED')),
    CONSTRAINT ck_reservations_quantity CHECK (quantity > 0)
);

CREATE INDEX idx_reservations_order ON inventory_reservations (order_id);

-- -----------------------------------------------------------------------------
-- Fulfillment, audit, and notifications
-- -----------------------------------------------------------------------------
CREATE TABLE shipments (
    id              BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version         BIGINT      NOT NULL DEFAULT 0,
    created_at      TIMESTAMP   NOT NULL,
    updated_at      TIMESTAMP   NOT NULL,
    order_id        BIGINT      NOT NULL,
    warehouse_id    BIGINT      NOT NULL,
    carrier         VARCHAR(80),
    tracking_number VARCHAR(120),
    status          VARCHAR(20) NOT NULL,
    CONSTRAINT fk_shipments_order     FOREIGN KEY (order_id)     REFERENCES orders (id),
    CONSTRAINT fk_shipments_warehouse FOREIGN KEY (warehouse_id) REFERENCES warehouses (id),
    CONSTRAINT ck_shipments_status CHECK (status IN ('PENDING', 'SHIPPED', 'DELIVERED'))
);

CREATE INDEX idx_shipments_order ON shipments (order_id);

CREATE TABLE audit_logs (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version     BIGINT      NOT NULL DEFAULT 0,
    created_at  TIMESTAMP   NOT NULL,
    updated_at  TIMESTAMP   NOT NULL,
    entity_type VARCHAR(60) NOT NULL,
    entity_id   BIGINT,
    action      VARCHAR(60) NOT NULL,
    actor       VARCHAR(120),
    details     TEXT
);

CREATE INDEX idx_audit_entity ON audit_logs (entity_type, entity_id);

CREATE TABLE notifications (
    id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    version     BIGINT      NOT NULL DEFAULT 0,
    created_at  TIMESTAMP   NOT NULL,
    updated_at  TIMESTAMP   NOT NULL,
    customer_id BIGINT      NOT NULL,
    type        VARCHAR(60) NOT NULL,
    channel     VARCHAR(40) NOT NULL,
    payload     TEXT,
    status      VARCHAR(20) NOT NULL
);

CREATE INDEX idx_notifications_customer ON notifications (customer_id);

